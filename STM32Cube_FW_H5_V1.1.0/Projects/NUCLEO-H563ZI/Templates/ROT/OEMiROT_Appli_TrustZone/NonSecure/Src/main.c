/**
  * @brief  Main program
  * @retval None
  */
int main(void)
{
  /* STM32H5xx **NON-SECURE** HAL library initialization:
       - Configure the Flash prefetch
       - Configure the Systick to generate an interrupt each 1 msec
       - Set NVIC Group Priority to 4
       - Low Level Initialization
     */
  HAL_Init();

  /* Register SecureFault callback defined in non-secure and to be called by secure handler */
  SECURE_RegisterCallback(SECURE_FAULT_CB_ID, (void *)SecureFault_Callback);

  /* Register SecureError callback defined in non-secure and to be called by secure handler */
  SECURE_RegisterCallback(GTZC_ERROR_CB_ID, (void *)SecureError_Callback);

 /* Configure the System clock to have a frequency of 250 MHz (PLL) */
  SystemClock_Config();

  /* Add your non-secure example code here
     */

  /* Enable GPIOB clock */
  __HAL_RCC_GPIOB_CLK_ENABLE();

  GPIO_Structure_Init.Mode = GPIO_MODE_OUTPUT_PP;
  GPIO_Structure_Init.Pull = GPIO_NOPULL;
  GPIO_Structure_Init.Pin = GPIO_PIN_0;
  GPIO_Structure_Init.Speed = GPIO_SPEED_FREQ_LOW;

  HAL_GPIO_Init(GPIOB, &GPIO_Structure_Init);

  /* Infinite loop */
  while (1)
  {
    HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_0);
    HAL_Delay(500);
  }
}

